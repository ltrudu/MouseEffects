namespace MouseEffects.Effects.SacredGeometries;

/// <summary>
/// Sacred geometry pattern types for the Mandala effect.
/// </summary>
public enum PatternType
{
    /// <summary>7 overlapping circles forming the seed pattern.</summary>
    SeedOfLife = 0,

    /// <summary>19 overlapping circles - the full flower pattern.</summary>
    FlowerOfLife = 1,

    /// <summary>2 overlapping circles creating the almond/fish shape.</summary>
    VesicaPiscis = 2,

    /// <summary>Star tetrahedron - 2 interlocking triangles.</summary>
    Merkaba = 3,

    /// <summary>9 interlocking triangles forming a sacred Hindu symbol.</summary>
    SriYantra = 4,

    /// <summary>13 circles with lines connecting all centers.</summary>
    MetatronsCube = 5,

    /// <summary>Kabbalistic symbol with 10 sephiroth and 22 paths.</summary>
    TreeOfLife = 6,

    /// <summary>Animated concentric rings creating 3D torus illusion.</summary>
    Torus = 7,

    /// <summary>Complex triangular lattice pattern.</summary>
    TetrahedronGrid = 8,

    /// <summary>Rotating 2D projections of the 5 platonic solids.</summary>
    PlatonicSolids = 9
}
